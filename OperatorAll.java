class OperatorAll
{
     public static void main(String [] args) 
	{
	   int a=2, b=5;
	   System.out.println("The demontration of Arithmetic operator ");
	   System.out.println(a+b);
	   System.out.println(a-b);
	   System.out.println(a*b);
	   System.out.println(a/b);
	   System.out.println(a%b);
	   System.out.println(" THe demostration of Assignment operator");
	   System.out.println(a+=b);
	   System.out.println(a-=b);
	   System.out.println(a*=b);
	   System.out.println(a/=b);
	   System.out.println(a%=b);
	   System.out.println("The demonstrating of in inc or dec operqtor");
	   System.out.println(a++ + b++);
	   System.out.println(++a + ++b);
	   System.out.println(a-- - b--);
	   System.out.println(--a - --b );
	}     
}
